// LOOP - DO WHILE

let counter = 0;                          // Variable that starts at 0

// The code inside "do" runs at least one time
do {
    console.log(`Counter: ${counter}`);   // Print the current value of counter
    counter++;                            // Increase counter by 1
} while (counter < 5);                    // Repeat while counter is less than 5

console.log("End of do while loop");                 // This line runs when the loop finishes
