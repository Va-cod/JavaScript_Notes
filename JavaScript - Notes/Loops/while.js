// LOOP WHILE

let counter = 0;                            // Variable that starts at 0

// The loop runs while counter is less than 5
while(counter < 5) {
    console.log(`Counter: ${counter}`);     // Print the current value of counter
    counter++;                              // Increase counter by 1 in each repetition
}
console.log("End of  while loop");                 // This line runs when the loop finishes
