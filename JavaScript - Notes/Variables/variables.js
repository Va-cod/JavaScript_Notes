// JAVASCRIPT DATA TYPES EXAMPLE

// String - Stores text values inside quotes.
let name = "Carlos Sainz";

// Number - Stores numeric values (integers or decimals).
let age = 30;

// String - Another text value representing a country.
let country = "Spain";

// Boolean - Stores only true or false values.
let isDriver = true;

// String - Stores the name of the racing team.
let racingTeam = "Williams Racing";

// Null - Represents an intentional empty value.
// It means the variable exists but has no value assigned yet.
let champions = null;

// Undefined - The variable is declared but no value has been assigned.
let singleSeater;

// PRINTING VARIABLE VALUES
console.log("Driver Name:", name);
console.log("Age:", age);
console.log("Country:", country);
console.log("Is Formula 1 Driver?:", isDriver);
console.log("Racing Team:", racingTeam);
console.log("Championships Won:", champions);
console.log("Single Seater Car:", singleSeater);

// PRINTING DATA TYPES
// typeof is used to know the type of data stored in a variable.
console.log("\nType of name:", typeof name);
console.log("Type of age:", typeof age);
console.log("Type of country:", typeof country);
console.log("Type of isDriver:", typeof isDriver);
console.log("Type of racingTeam:", typeof racingTeam);
console.log("Type of champions:", typeof champions); // Special case: returns "object"
console.log("Type of singleSeater:", typeof singleSeater);

